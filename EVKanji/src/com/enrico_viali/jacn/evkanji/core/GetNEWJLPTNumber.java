	public int getNewJLPTLevel(String k) {
	    if (level5.contains(k))
	        return 5;
	    if (level4.contains(k))
	        return 4;
	    if (level3.contains(k))
	        return 3;
	    if (level2.contains(k))
	        return 2;
	    if (level1.contains(k))
	        return 1;
	    log.warn("livello non trovato per: "+ k);
	    return 0;
	}
	
	
